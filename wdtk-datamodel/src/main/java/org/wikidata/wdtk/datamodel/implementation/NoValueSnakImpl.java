import org.wikidata.wdtk.datamodel.interfaces.NoValueSnak;
import org.wikidata.wdtk.datamodel.interfaces.PropertyId;

/**
 * Implementation of {@link NoValueSnak}.
 * 
 * @author Markus Kroetzsch
 * 
 */
public class NoValueSnakImpl extends SnakImpl implements NoValueSnak {

	public NoValueSnakImpl(PropertyId propertyId) {
		super(propertyId);
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#hashCode()
	 */
	@Override
	public int hashCode() {
		return this.propertyId.hashCode();
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#equals(java.lang.Object)
	 */
	@Override
	public boolean equals(Object obj) {
		if (obj == null)
			return false;
		if (obj == this)
			return true;
		if (!(obj instanceof NoValueSnak))
			return false;

		return this.propertyId.equals(((NoValueSnak) obj).getPropertyId());
	}

}
